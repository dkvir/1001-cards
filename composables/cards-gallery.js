  createInstancedMesh() {
    // Create the geometry for the plane
    const geometry = new THREE.PlaneGeometry(0.09, 0.2);

    // Initial material with a default color or texture (we will update it later)
    const material = new THREE.MeshBasicMaterial({ color: 0xcccccc });

    this.instancedMesh = new THREE.InstancedMesh(
      geometry,
      material,
      this.particles.length
    );

    this.instancedMesh.position.x = -0.5 * this.stringBox.wScene;
    this.instancedMesh.position.y = -0.6 * this.stringBox.hScene;

    this.instancedMesh.castShadow = true;

    // Create UVs for each particle/instance
    const uvsAttribute = new Float32Array(this.particles.length * 2); // Each particle needs 2 UV coordinates (x, y)

    this.particles.forEach((particle, idx) => {
      const uv = this.uvs[idx % this.uvs.length]; // Wrap around the UVs array if there are more particles than UVs
      uvsAttribute[idx * 2] = uv.x + uv.width * 0.5; // UV X
      uvsAttribute[idx * 2 + 1] = uv.y + uv.height * 0.5; // UV Y
    });

    const uvInstance = new THREE.InstancedBufferAttribute(uvsAttribute, 2);
    this.instancedMesh.geometry.setAttribute("uvInstance", uvInstance);

    this.scene.add(this.instancedMesh);

    // Now load the texture atlas and apply it to the material
    const textureLoader = new THREE.TextureLoader();
    textureLoader.load(
      "/images/large_atlas-min.png",
      (textureAtlas) => {
        console.log("alskfnsn");
        // Once the texture is loaded, update the material to use the atlas texture
        this.instancedMesh.material = new THREE.ShaderMaterial({
          uniforms: {
            textureAtlas: { value: textureAtlas },
          },
          vertexShader: `
            attribute vec2 uvInstance;
            varying vec2 vUv;
            void main() {
              vUv = uvInstance;
              vec4 mvPosition = modelViewMatrix * vec4(position, 1.0);
              gl_Position = projectionMatrix * mvPosition;
            }
          `,
          fragmentShader: `
            uniform sampler2D textureAtlas;
            varying vec2 vUv;
            void main() {
              gl_FragColor = texture2D(textureAtlas, vUv);
            }
          `,
          transparent: true,
        });

        // Once the texture is applied, we can start rendering
        this.instancedMesh.material.needsUpdate = true;
      },
      undefined, // onProgress callback (optional)
      (error) => {
        console.error("Error loading texture atlas:", error);
      }
    );
  }

  updateParticlesMatrices() {
    let idx = 0;
    this.particles.forEach((p) => {
      this.dummy.position.set(p.x, this.stringBox.hScene - p.y, p.z);
      this.dummy.scale.set(p.scale, p.scale, p.scale);
      this.dummy.updateMatrix();
      this.instancedMesh.setMatrixAt(idx, this.dummy.matrix);
      idx++;
    });
    this.instancedMesh.instanceMatrix.needsUpdate = true;
  }

  makeTextFitScreen() {
    const fov = this.camera.fov * (Math.PI / 180);
    const fovH = 2 * Math.atan(Math.tan(fov / 2) * this.camera.aspect);
    const dx = Math.abs((0.7 * this.stringBox.wScene) / Math.tan(0.5 * fovH));
    const dy = Math.abs((0.6 * this.stringBox.hScene) / Math.tan(0.5 * fov));
    const factor = Math.max(dx, dy) / this.camera.position.length();
    if (factor > 1) {
      this.camera.position.x *= factor;
      this.camera.position.y *= factor;
      this.camera.position.z *= factor;
    }
  }

  addEventListeners() {
    window.addEventListener("resize", () => {
      this.camera.aspect = window.innerWidth / window.innerHeight;
      this.camera.updateProjectionMatrix();
      this.renderer.setSize(window.innerWidth, window.innerHeight);
    });
  }
